// Variable Declaration in C
#include <stdio.h>

int main() {
    int age;
    age = 18;
    char grade = 'D';
    float years = 19.5;
    char name[] = "Abdul Rehman Kalsekar";

    printf("%d \n", age);
    printf("%f \n", years);
    printf("%c \n", grade);
    printf("%s \n", name);
    return 0;
}